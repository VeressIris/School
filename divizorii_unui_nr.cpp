#include <iostream>

using namespace std;

int main()
{
    int n;
    cin >> n;

    int nrDiv = 0;
    cout << "Divizorii lui " << n << " sunt: ";
    for (int i = 1; i <= n; i++)
    {
        if (n % i == 0)
        {
            cout << i << ' ';
            nrDiv++;
        }
    }

    cout << '\n' << "Are " << nrDiv << " divizori";

    return 0;
}
